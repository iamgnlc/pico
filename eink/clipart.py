def _bounds(w, h, data):
    top, bottom = ink_bounds(w, h, data)
    return (0, h - 1) if bottom < 0 else (top, bottom)


_BOUNDS = {name: _bounds(w, h, d) for name, (w, h, d) in CLIPART.items()}


def _eff_scale(scale):
    # 32x32 art rendered at half the caller's scale so on-screen size stays
    # roughly matched to text height. At TEXT_SCALE 1-2 this is 1x native.
    return max(1, scale // 2)


def draw_clipart(fb, name, x, y, color, scale):
    # Draw CLIPART[name] so (x, y) aligns with the top of the actual ink,
    # matching text_render.big_text semantics.
    w, h, data = CLIPART[name]
    rb = (w + 7) // 8
    top, _ = _BOUNDS[name]
    s = _eff_scale(scale)
    for cy in range(h):
        for cx in range(w):
            if pixel(data, rb, cx, cy):
                fb.fill_rect(x + cx * s,
                             y + (cy - top) * s,
                             s, s, color)


def clipart_size(name, scale):
    # (width, trimmed height) — height matches text_render.text_height.
    w, h, _ = CLIPART[name]
    top, bottom = _BOUNDS[name]
    s = _eff_scale(scale)
    return w * s, (bottom - top + 1) * s
